package com.jevetools.bulkdata.model.cli.internal;

import org.eclipse.osgi.framework.console.CommandProvider;
import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;
import org.osgi.framework.ServiceRegistration;

import com.jevetools.bulkdata.model.cli.CommandsProvider;

/**
 * Copyright (c) 2013, jEVETools.
 * 
 * All rights reserved.
 * 
 * @version 0.0.1
 * @since 0.0.1
 */
public final class Activator
    implements BundleActivator
{
  /**
   * {@link CommandsProvider}.
   */
  private final transient CommandsProvider mService = new CommandsProvider();

  /**
   * {@link ServiceRegistration}.
   */
  private transient ServiceRegistration<CommandProvider> mRegisterService;

  @Override
  public void start(final BundleContext aContext) throws Exception // NOPMD
  {
    mRegisterService = aContext.registerService(CommandProvider.class,
        mService, null);
  }

  @Override
  public void stop(final BundleContext aContext) throws Exception // NOPMD
  {
    aContext.ungetService(mRegisterService.getReference());
    mRegisterService.unregister();
  }
}
